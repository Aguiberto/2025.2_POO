public class Sequencia1{
    public static void main(String[] args) {
        
        int j = 60;
        int i = 1;
     
        System.out.println("I" + "=" + i +" "+ "J" + "=" + j);
    
        while( j > 0){
            j -= 5;
            i +=3;
        System.out.println("I" + "=" + i +" "+ "J" + "=" + j);
        }

    }
}